    private void resfreshDataTable(){
        DefaultTableModel model=(DefaultTableModel) tblExtrait.getModel();
        model.setRowCount(0);
        List<ExtraitModel> data=extraitImpl.getListExtrait();
        for(ExtraitModel item:data){
            Vector<String>vec=new Vector<>();
            vec.add(String.valueOf(item.getNumAct()));
            vec.add(item.getPrenom());
            vec.add(item.getNom());
            vec.add(item.getLieu());
            vec.add(item.getDateNaissance().toString());
            vec.add(item.getSexe());
            vec.add(item.getPrenomPere());
            vec.add(item.getNomPere());
            vec.add(item.getDomicilePere());
            vec.add(item.getProfessionPere());
            vec.add(item.getPrenomMere());
            vec.add(item.getNomMere());
            vec.add(item.getDomicileMere());
            vec.add(item.getProfessionMere());
            vec.add(item.getDateDelivrance().toString());
            model.addRow(vec);
        }
    }
