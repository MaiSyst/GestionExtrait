    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        String prenom=tfPrenom.getText();
        String nom=tfNom.getText();
        String date=tfDate.getText();
        String dateDelivrance=tfDateDelivrance.getText();
        String domicileMere=tfDomicileMere.getText();
        String domicilePere=tfDomicilePere.getText();
        String lieu=tfLieu.getText();
        String nomMere=tfNomMere.getText();
        String nomPere=tfNomPere.getText();
        String prenomPere=tfPrenomPere.getText();
        String professionPere=tfProfessionPere.getText();
        String professionMere=tfProfessionMere.getText();
        String prenomMere=tfPrenomMere.getText();
        String sexe=comboSexe.getSelectedItem().toString();
            
        if (prenom.isBlank()||nom.isBlank()||date.isBlank()||
                dateDelivrance.isBlank()||domicileMere.isBlank()||
                domicilePere.isBlank()||lieu.isBlank()||nomMere.isBlank()||
                nomPere.isBlank()||prenomPere.isBlank()||professionPere.isBlank()||
                professionMere.isBlank()||prenomMere.isBlank()) {
                        JOptionPane.showMessageDialog(null, "Desoler verifier vos informations.");    
        }else{
             SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
            
            try {
                Date d = new Date(sdf.parse(date).getTime());
                Date dDeliv = new Date(sdf.parse(dateDelivrance).getTime());
                ExtraitModel model =new ExtraitModel(
                        prenom, nom, dDeliv, lieu, sexe, prenomPere,
                        nomPere, domicilePere, professionPere, prenomMere, nomMere, domicileMere, 
                        professionMere, dDeliv);
                extraitImpl.ajouterExtrait(model);
                resfreshDataTable();
                clearFields();
                
            } catch (ParseException ex) {
                Logger.getLogger(ExtraitForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAjouterActionPerformed

